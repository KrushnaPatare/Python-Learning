'''Operators in Python --->
   1)Arithmatic:   +, -, /, *, %
   2)Assignment:   +=, -=, /=, *=, %=
   3)Comparision(Relational):  <, >, <=, >=, ==, !=
   4)Logical: and, or, not
'''
a=10
b=50
c=1000
d=10000
e=20

#1]Arithmatic Operator--->
print("1]Arithmatic Operator")
print(b+c)
print(c-b)
print(d/c)
print(b*c)
print(b%e)
print()

#2]Assignment Operator--->
print("2]Assignment Operator")
a+=5; print(a)
c-=50; print(c)
b*=25; print(b)
d/=50; print(d)
d%=35; print(d)
print()

#3]Comparision Operator--->
print("3]Comparision Operator")
print(10<100)
print(20<5)
print(e==20)
print(50<=50)
print(100>=500)
print(100!=500)
print()

#4]Relational Operator--->
print("4]Relational Operator")
print(10<20 and 100>50)
print(10>20 and 100>50)
print(10>20 or 100>50)
print(10>20 or 100<50)
print(not 30> 20)
print()

